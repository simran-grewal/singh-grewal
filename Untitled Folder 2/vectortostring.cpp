#include<bits/stdc++.h>
using namespace std;
int main()
{
    vector<string> str;
    for(int i=0;i<5;i++)
    {
        string s;
        cin>>s;
        str.push_back(s);
    }
    string sim;
    for(int i=0;i<str.size();i++)
    {
        string ss=str[i];
        int j=0;   
        while(ss[j]!='\0')
        {
            sim.push_back(ss[j]);
                            j++;
        }
        char space=' ';
        sim.push_back(space); 
       
    }
    cout<<sim;
}
